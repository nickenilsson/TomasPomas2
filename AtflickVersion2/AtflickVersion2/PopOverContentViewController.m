@interface PopOverContentViewController ()

@property (strong, nonatomic) NSMutableArray *menuTitles;
@property (strong, nonatomic) NSMutableArray *menuOptions;

@end

@implementation PopOverContentViewController

-(id)initWithMenuTitles:(NSMutableArray *)menuTitles menuOptions:(NSMutableArray *)menuOptions
{
    self = [super init];
    if (self) {
        self.menuTitles = menuTitles;
        self.menuOptions = menuOptions;
        
        self.clearsSelectionOnViewWillAppear = NO;
        
        //Calculate how tall the view should be by multiplying
        //the individual row height by the total number of rows.
        NSInteger rowsCount = [self.menuTitles count];
        NSInteger singleRowHeight = [self.tableView.delegate tableView:self.tableView
                                               heightForRowAtIndexPath:[NSIndexPath indexPathForRow:0 inSection:0]];
        NSInteger totalRowsHeight = rowsCount * singleRowHeight;
        
        //Calculate how wide the view should be by finding how
        //wide each string is expected to be
        CGFloat largestLabelWidth = 0;
        for (NSString *title in self.menuTitles) {
            //Checks size of text using the default font for UITableViewCell's textLabel.
            CGSize labelSize = [title sizeWithFont:[UIFont boldSystemFontOfSize:20.0f]];
            if (labelSize.width > largestLabelWidth) {
                largestLabelWidth = labelSize.width;
            }
        }
        
        //Add a little padding to the width
        CGFloat popoverWidth = largestLabelWidth + 100;
        
        //Set the property to tell the popover container how big this view will be.
        self.contentSizeForViewInPopover = CGSizeMake(popoverWidth, totalRowsHeight);

    }
    return self;
}
